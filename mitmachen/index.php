<h3>Wo kann ich mit machen?</h3>
<p>Du kannst einen Router quasi überall aufbauen. Am Besten gibt es vor Ort eine Möglichkeit den Router ans Internet anzuschließen oder es gibt bereits Freifunk-Nachbarn.</p>
<p>Sieh auf der Karte nach, ob bereits Knotenpunkte in deiner Nachbarschaft sind mit denen sich dein Router verbinden kann. Du kannst auch zusammen mit anderen einen Aufbau planen. Das <!--FIXME: Wiki-Seite vorher ueberarbeiten-->Weimarnetz-Treffen findet jeden Diestag im <a href="http://blog.maschinenraum.tk/contact/">Maschinenraum</a> statt. Wenn Du nicht zum Treffen kommen kannst, erreichst Du den Verein auch über die <a href="http://wireless.subsignal.org/index.php?title=Mailingliste">Mailingliste</a>, die <a href="mailto:kontakt@weimarnetz.de">Kontakt-E-Mail-Adresse</a>, oder auch über das Weimarnetz Sorgentelefon unter +49 3643 544304.</p>
