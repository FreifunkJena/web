<h3>Weitere Informationen und Hinweise</h3>
<dl><dt>Was ist ein Mesh-Netzwek? Was ist ein VPN?<dt>
<dd>Wenn ein Freifunk-Router direkt oder indirekt über andere das Internet erreichen kann, wird er automatisch Teil des <em>Virtual Private Network</em>.<br \>
Jeder Router der sich mit anderen Routern über Funk verbinden kann, bildet zusammen mit diesen ein Maschennetz (engl. Mesh-Network). Das Mesh-Net beschreibt die besondere Eigenschaft des Netzwerks &mdash; auch ohne Kabel &mdash; Teilnehmer unterschiedlicher Router miteinander zuverbinden.<br \>
Das Freifunk Wiki erklärt VPN:
<blockquote>Ein Virtual Private Network [...] ist ein Computernetz, das zum Transport privater Daten ein öffentliches Netz (zum Beispiel das Internet) nutzt. Teilnehmer eines VPN können Daten wie in einem lokalen Netzwerk (LAN) austauschen. Die einzelnen Teilnehmer selbst müssen hierzu nicht direkt verbunden sein. Die Verbindung über das öffentliche Netz wird üblicherweise verschlüsselt. [...] Mit Hilfe eines VPN besteht die Möglichkeit getrennte weit auseinander gelegene Funknetze, zum Beispiel verschiedene Freifunk-Netze, über eine Datenleitung miteinander zu verbinden &ndash; <a href="http://wiki.freifunk.net/VPN">wiki.freifunk.net/VPN</a></blockquote></dd>
</dl>
</p>

